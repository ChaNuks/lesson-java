package lesson.java.solid.assignment.srpExample.CorrectExample;

public class IceCream {

    private String menu;
    private int price;

    public IceCream(String menu, int price) {
        this.menu = menu;
        this.price = price;
    }

    public String getMenu() {
        return this.menu;
    }

    public int getPrice() {
        return this.price;
    }
}
